const STOP_PATTERN = [2,3,3,1,1,1,2];

export function encodeCode128(text: string): boolean[] {
  const codes: number[] = [CODE128B_START];
  for (let i = 0; i < text.length; i++) {
    const code = text.charCodeAt(i) - 32;
    if (code >= 0 && code < 95) codes.push(code);
  }
  // checksum
  let checksum = codes[0];
  for (let i = 1; i < codes.length; i++) checksum += codes[i] * i;
  codes.push(checksum % 103);
  codes.push(CODE128_STOP);

  const bars: boolean[] = [];
  for (let i = 0; i < codes.length; i++) {
    const pattern = i === codes.length - 1 ? STOP_PATTERN : PATTERNS[codes[i]];
    if (!pattern) continue;
    for (let j = 0; j < pattern.length; j++) {
      const isBar = j % 2 === 0;
      for (let k = 0; k < pattern[j]; k++) bars.push(isBar);
    }
  }
  return bars;
}

export function renderBarcodeToDataURL(text: string, width = 300, height = 60): string {
  const bars = encodeCode128(text);
  const canvas = document.createElement("canvas");
  canvas.width = width;
  canvas.height = height;
  const ctx = canvas.getContext("2d");
  if (!ctx) return "";
  ctx.fillStyle = "#ffffff";
  ctx.fillRect(0, 0, width, height);
  ctx.fillStyle = "#000000";
  const barWidth = width / bars.length;
  for (let i = 0; i < bars.length; i++) {
    if (bars[i]) ctx.fillRect(i * barWidth, 0, barWidth, height);
  }
  return canvas.toDataURL("image/png");
}
